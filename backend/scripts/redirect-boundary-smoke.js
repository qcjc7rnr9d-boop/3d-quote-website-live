import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '../..');

function read(path) {
  return readFileSync(resolve(root, path), 'utf8');
}

const customerLogin = read('customer/login.html');
const quote = read('quote.html');
const materials = read('materials.html');
const options = read('options.html');
const checkout = read('assets/checkout.js');
const stripeRoutes = read('backend/routes/stripe.js');

assert.match(
  customerLogin,
  /\^dashboard\\\.html\(\?:\\\?\|#\|\$\)/,
  'customer login should only honor same-page dashboard.html next targets',
);
assert.doesNotMatch(
  customerLogin,
  /window\.location\.href\s*=\s*nextPath/,
  'customer login should not redirect directly to an unvalidated next parameter',
);
assert.match(
  quote,
  /next = `dashboard\.html\?shop=\$\{encodeURIComponent\(SHOP_SLUG\)\}#quotes`/,
  'save-quote auth prompt should generate a same-origin dashboard next target',
);
assert.match(
  materials,
  /index\.html\?shop=\$\{encodeURIComponent\(SHOP_SLUG\)\}&newGroup=1&promptUpload=1/,
  'materials missing-upload guard should route to the home upload prompt',
);
assert.match(
  options,
  /index\.html\?shop=\$\{encodeURIComponent\(SHOP_SLUG\)\}&newGroup=1&promptUpload=1/,
  'options missing-upload guard should route to the home upload prompt',
);
assert.doesNotMatch(
  options,
  /quote\.html\?shop=\$\{encodeURIComponent\(SHOP_SLUG\)\}&newGroup=1&promptUpload=1/,
  'options missing-upload guard should not route to the quote-page upload prompt',
);
assert.match(
  quote,
  /index\.html\?shop=\$\{encodeURIComponent\(SHOP_SLUG\)\}&newGroup=1&promptUpload=1/,
  'add-another action should return to the home upload prompt',
);
assert.match(
  checkout,
  /confirmation\.pathname \+ confirmation\.search/,
  'checkout confirmation redirect should keep navigation local',
);
assert.match(
  stripeRoutes,
  /const baseUrl = process\.env\.BASE_URL \|\| 'http:\/\/localhost:3000'/,
  'Stripe Connect redirects should use configured platform base URL, not request-controlled host headers',
);
assert.match(
  stripeRoutes,
  /stripe\.accounts\.createLoginLink\(shop\.stripe_account_id\)/,
  'Stripe dashboard links should be generated by Stripe for the connected account',
);

console.log('Redirect boundary smoke checks passed.');
